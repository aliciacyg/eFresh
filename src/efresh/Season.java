package efresh;

// -------------------------------------------------------------------------
/**
 * Season of the year
 * 
 * @author alici
 * @version May 23, 2024
 */
public enum Season
{
    /**
     * Spring season, where flowers bloom, lettuce grow, turnips sprout
     */
    SPRING,
    /**
     * Summer season, where fruits are abundant. Peaches, tomatoes, eggplant yum
     */
    SUMMER,
    /**
     * Fall season, where squashes and apples. Red and orange abound.
     */
    FALL,
    /**
     * Winter season, where nothing grows. Hope you stored enough beans to last
     */
    WINTER,
    /**
     * Non-seasonal items
     */
    ALL;
}
